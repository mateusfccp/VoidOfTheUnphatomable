package org.pintoschneider.void_of_the_unfathomable;

/**
 * Enum representing various status effects that can affect a player or monster.
 */
public enum StatusEffect {
    /**
     * The entity is poisoned and takes damage over time.
     */
    POISONED,

    /**
     * The entity is stunned and cannot take actions for a duration.
     */
    STUNNED,

    /**
     * The entity is burning and takes fire damage over time.
     */
    BURNING
}
