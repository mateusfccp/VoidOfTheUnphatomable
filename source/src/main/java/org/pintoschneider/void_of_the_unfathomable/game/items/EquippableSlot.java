package org.pintoschneider.void_of_the_unfathomable.game.items;

/**
 * An enumeration representing the different slots where equippable items can be placed.
 * <p>
 * Each slot corresponds to a specific type of equipment that a character can wear or use, and a slot can only hold
 * one item of the appropriate type at a time.
 */
public enum EquippableSlot {
    /**
     * Slot for weapons.
     */
    WEAPON,
    /**
     * Slot for armors.
     */
    ARMOR
}
