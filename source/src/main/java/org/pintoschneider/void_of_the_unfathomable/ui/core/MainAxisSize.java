package org.pintoschneider.void_of_the_unfathomable.ui.core;

/**
 * An enumeration that represents the size options for the main axis in a layout.
 */
public enum MainAxisSize {
    /**
     * The main axis should take up as much space as its children need.
     */
    MIN,

    /**
     * The main axis should take up all available space.
     */
    MAX
}
